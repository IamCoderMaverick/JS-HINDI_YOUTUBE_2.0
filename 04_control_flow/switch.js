const month = "mar"


switch (month) {
    case "jan": 
        console.log("This is January");
        break;
    case "feb": 
        console.log("This is FEB");
        break;
    case "mar": 
        console.log("This is MAR");
        break;
    case "apr": 
        console.log("This is APR");
        break;
    default:
        console.log("Default case matched");
        break;
}






